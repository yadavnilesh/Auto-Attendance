void typsearch()
{
    try{
            model.setRowCount(0);
            clearall();
            String sh=searchtext.getText().trim();
            PreparedStatement pst;
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/facedb?" + "user=root&password=root");
            if(rdname.isSelected())
            {
            pst = conn.prepareStatement("Select  * from course where course like '%" + sh + "%'");
            ResultSet rs=pst.executeQuery();
            int i=0;
            if(rs.next())
            {
                do{
                   
                    model.insertRow(i,new Object[] { rs.getString("course"),rs.getString("year")});
                    i++;
                }while(rs.next());
            }
            }
            else if(rdyear.isSelected())
            {
            pst = conn.prepareStatement("Select  * from course where year like '%" + sh + "%'");
            ResultSet rs=pst.executeQuery();
            int i=0;
            if(rs.next())
            {
                do{
                   
                    model.insertRow(i,new Object[] { rs.getString("course"),rs.getString("year")});
                    i++;
                }while(rs.next());
            }
            }                

       
            }catch(Exception ex){
                ex.printStackTrace();
            }
}
