    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
               if(jTable1.getSelectionModel().isSelectionEmpty())
               {
                 JOptionPane.showMessageDialog(null, "please first select an entry form the table which you want to update");
               }        
               else  try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/facedb?" + "user=root&password=root");
            PreparedStatement pst = conn.prepareStatement("update course set course=?,year=? where course=?;");
            pst.setString(1,name.getText());
            pst.setString(2, year.getSelectedItem().toString());
            pst.setString(3,jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            int rs=pst.executeUpdate();
            if(rs!=0)
            {
               model.setRowCount(0);
               insert();
               clearall();
            }
            }catch(Exception ex){
                ex.printStackTrace();
            }
     
    }//GEN-LAST:event_updateActionPerformed

