    public teacher(ResultSet rs) {
        initComponents();
        this.rs=rs;
        try {
            username.setText("Hi "+rs.getString("userid"));
            ByteArrayInputStream bais = new ByteArrayInputStream(rs.getBytes("profilepic"));
            BufferedImage bu= ImageIO.read(bais);
            Image resized =  bu.getScaledInstance( profilepic.getWidth(), profilepic.getHeight(), Image.SCALE_SMOOTH);
            BufferedImage buffered = new BufferedImage( profilepic.getWidth(), profilepic.getHeight(), Image.SCALE_REPLICATE);
            buffered.getGraphics().drawImage(resized, 0, 0 , null);
            profilepic.setIcon(new ImageIcon(buffered));
        }
        catch (IOException ex) {
            Logger.getLogger(teacher.class.getName()).log(Level.SEVERE, null, ex);
        }
         catch (SQLException ex) {
            Logger.getLogger(teacher.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

